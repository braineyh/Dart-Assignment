void main() {
  // Integer Data Type
  int age = 25;

  // Double Data Type
  double price = 19.99;

  // String Data Type
  String name = "John Smith";

  // Boolean Data Type
  bool isStudent = true;

  // List Data Type
  List<String> fruits = ["Apple", "Banana", "Orange"];

  // Map Data Type
  Map<String, dynamic> person = {
    "name": "Alice",
    "age": 30,
    "isStudent": false
  };

  // Printing the values
  print("Age: $age");
  print("Price: \$$price");
  print("Name: $name");
  print("Is a Student: $isStudent");
  print("Fruits: $fruits");
  print("Person: $person");
}
