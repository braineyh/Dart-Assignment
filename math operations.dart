void main() {
  // Declare two variables
  int a = 10;
  int b = 5;

  // Addition
  int sum = a + b;
  print("Sum: $sum");

  // Subtraction
  int difference = a - b;
  print("Difference: $difference");

  // Multiplication
  int product = a * b;
  print("Product: $product");

  // Division
  double quotient = a / b;
  print("Quotient: $quotient");

  // Modulus (Remainder)
  int remainder = a % b;
  print("Remainder: $remainder");
}
